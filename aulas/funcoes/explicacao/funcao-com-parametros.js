// Criar função para cumprimentar usuário
function cumprimentar (pessoa, idade) { // parâmetros só existem nas funções
    console.log(`Olá, ${pessoa} fiquei sabendo que você tem ${idade} anos.`);
}

cumprimentar('Pâmela', 21); // valor da variável é argumento
cumprimentar('Taiane', 34);
cumprimentar('Renata', 16);
cumprimentar('Mainha', 19);