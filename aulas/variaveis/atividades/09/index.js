// Soma dos ângulos internos de um polígono

let quantidadeLadosPoligonos = 6;
const somaAngulos = (quantidadeLadosPoligonos - 2) * 180;
const valorAngulo = (somaAngulos / quantidadeLadosPoligonos);

console.log(`Em um poligono que possui ${quantidadeLadosPoligonos} lados, sua soma resulta em ${somaAngulos} e o valor do ângulos ${valorAngulo} `)

