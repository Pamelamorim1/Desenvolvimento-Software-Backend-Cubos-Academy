let valorX = 8, valorY = 2;

let resultado = 'A soma de ' + valorX + ' e ' + valorY + ' é ' + (valorX + valorY);
console.log(resultado); //10

resultado = `A multiplicação de ${valorX} e ${valorY} é ${valorX * valorY}`;
console.log(resultado); //16

resultado = `A subtração de ${valorX} e ${valorY} é ${valorX - valorY}`;
console.log(resultado); //6

resultado = `A divisão de ${valorX} e ${valorY} é ${valorX / valorY}`;
console.log(resultado); //4

resultado = `O resto da divisão de ${valorX} e ${valorY} é ${valorX % valorY}`;
console.log(resultado); //0
